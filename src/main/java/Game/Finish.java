package Game;

public class Finish extends Positioning {
    private int finishXPosition;
    private int finishYPosition;

    Finish(){
        finishXPosition =1;
        finishYPosition =3;
    }


    public int getFinishXPosition() {
        return finishXPosition;
    }

    public int getFinishYPosition() {
        return finishYPosition;
    }

}
